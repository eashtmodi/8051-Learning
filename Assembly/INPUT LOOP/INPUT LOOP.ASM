ORG 00H;
LOOP:MOV P0,#1H;
    LCALL DELAY;
	MOV A,#1H;
    MOV R2,#8;
HEREE:MOV B,#2H;
	SETB P3.6;
	SETB P3.7;
BACK: JNB P3.6,NXT;
	LCALL DELAY;
	SJMP BACK;
    NXT: MUL AB;
	MOV P0,A;
	LCALL DELAY;
	DJNZ R2,HEREE;
	SJMP LOOP;

ORG 300H;
	DELAY:MOV R0,#255;
	AGAIN:MOV R1,#200;
	HERE:DJNZ R1,HERE;
	DJNZ R0,AGAIN;
	RET;
END;